    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // buscar si existe el usuario 
        // en el arraylist de empleados de la empresa, se lo paso a empresa
        // creo la busqueda en empresa
        String login=txtLogin.getText();
        String pass=txtContra.getText();
        int pos = email.buscarEmpleado(login, pass);
        if(pos==-1){
            JOptionPane.showMessageDialog(this, "Este usuario no existe o contrase침a incorrecta");
            //txtLogin.setText("");  // borramos o no cajas
            //txtContra.setText("");  // borramos o no cajas
            txtLogin.requestFocus(); // poner foco
        }else{
            int id=email.getMisEmpleados().get(pos).getIdentificador();
            int tipo=email.getMisEmpleados().get(pos).getTipoUsuario();
            if(tipo==1){
                new Administrador(email,id).setVisible(true); // creo y muestro la ventana de tipo Administrador
                dispose(); // destruyo la actual, la de Validaci칩n
                pack(); // redibujar
            }else{
                new Usuario(email,id).setVisible(true); // creo y muestro la ventana de tipo Usuario
                dispose(); // destruyo la actual, la de Validaci칩n
                pack(); // redibujar
            }
            
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

